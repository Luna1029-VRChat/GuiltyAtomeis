proc calculateFnv1a(filePath: string): uint64 =
  var f = open(filePath, fmRead)
  defer: f.close()
  result = 0xcbf29ce484222325'u64
  var buf = newSeq[uint8](4096)
  while not f.endOfFile:
    let bytesRead = f.readBytes(buf, 0, 4096)
    if bytesRead == 0: break
    for i in 0 ..< bytesRead:
      result = result xor uint64(buf[i])
      result = result * 0x00000100000001B3'u64

proc signBinary(filePath: string) =
  let hashVal = calculateFnv1a(filePath)
  var f = open(filePath, fmAppend)
  defer: f.close()
  var h = hashVal
  for i in 0..7:
    f.write(char(h and 0xFF))
    h = h shr 8
  f.write("ATMX")

