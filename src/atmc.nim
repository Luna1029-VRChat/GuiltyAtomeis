proc parseArgs(): CompileOptions =
  var p = initOptParser()
  result.mode = modeExe
  result.payloadDir = getCurrentDir()
  while true:
    p.next()
    case p.kind
    of cmdEnd: break
    of cmdShortOption, cmdLongOption:
      case p.key
      of "debug": result.mode = modeDebug
      of "atb": result.mode = modeAtb
      of "dir": result.payloadDir = p.val
      else: discard
    of cmdArgument:
      if result.srcFile == "": result.srcFile = p.key
      elif result.outFile == "": result.outFile = p.key

  if result.srcFile == "":
    showUsage()
    quit(1)

proc compileSrc(opts: CompileOptions): tuple[bytecode: seq[uint8], pool: seq[string], externs: seq[string]] =
  if opts.srcFile.endsWith(".py"):
    let (bc, pool) = transpileWithAtb(opts.srcFile, "python_trans.atb")
    return (bc, pool, @[])

  let input = readFile(opts.srcFile)
  var tokens = tokenize(input)
  var p = Parser(tokens: tokens, pos: 0)
  let ast = p.parseProgram()
  return generateProgram(ast)

