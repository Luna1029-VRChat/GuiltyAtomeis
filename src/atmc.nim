proc main() =
  let opts = parseArgs()
  case opts.mode
  of modeAtb:
    let (bytecode, _, _) = compileSrc(opts)
    writeFile(opts.outFile, cast[string](bytecode))
  of modeDebug:
    if opts.srcFile == "" or opts.outFile == "": quit(1)
    let (bytecode, pool, _) = compileSrc(opts)
    buildSingleBinary(opts, bytecode, pool, false)
    echo "[+] EXE written (debug): ", opts.outFile
  of modeExe:
    if opts.srcFile == "" or opts.outFile == "": quit(1)
    let (bytecode, pool, _) = compileSrc(opts)
    buildSingleBinary(opts, bytecode, pool, true)
    echo "[+] EXE written: ", opts.outFile

